create table if not exists public.learning_tabs (
  id bigint generated by default as identity primary key,
  slug text not null unique,
  title text not null,
  artist text not null,
  artist_slug text not null,
  status text not null default 'published' check (status in ('draft', 'private', 'published')),
  source_type text not null default 'user' check (source_type in ('user', 'import', 'ai', 'licensed', 'demo')),
  tuning text,
  capo text,
  bpm integer,
  key text,
  instruments text[] not null default '{}',
  tab_text text not null default '',
  contributor_id uuid references auth.users(id) on delete set null,
  contributor_name text,
  revision_number integer not null default 1,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.learning_tab_tracks (
  id bigint generated by default as identity primary key,
  tab_id bigint not null references public.learning_tabs(id) on delete cascade,
  name text not null,
  instrument text not null,
  tuning text,
  volume integer not null default 80,
  muted boolean not null default false,
  solo boolean not null default false,
  tab_text text not null default '',
  midi_data jsonb,
  position integer not null default 1,
  created_at timestamptz not null default now()
);

create table if not exists public.learning_tab_revisions (
  id bigint generated by default as identity primary key,
  tab_id bigint not null references public.learning_tabs(id) on delete cascade,
  user_id uuid references auth.users(id) on delete set null,
  revision_number integer not null,
  change_note text,
  tab_snapshot jsonb not null,
  created_at timestamptz not null default now(),
  unique (tab_id, revision_number)
);

create table if not exists public.learning_playlists (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  name text not null,
  description text,
  created_at timestamptz not null default now(),
  updated_at timestamptz not null default now()
);

create table if not exists public.learning_playlist_items (
  id bigint generated by default as identity primary key,
  playlist_id bigint not null references public.learning_playlists(id) on delete cascade,
  tab_id bigint not null references public.learning_tabs(id) on delete cascade,
  position integer not null default 1,
  created_at timestamptz not null default now(),
  unique (playlist_id, tab_id)
);

create table if not exists public.learning_favorites (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  tab_id bigint not null references public.learning_tabs(id) on delete cascade,
  created_at timestamptz not null default now(),
  unique (user_id, tab_id)
);

create table if not exists public.learning_history (
  id bigint generated by default as identity primary key,
  user_id uuid not null references auth.users(id) on delete cascade,
  tab_id bigint not null references public.learning_tabs(id) on delete cascade,
  opened_at timestamptz not null default now(),
  unique (user_id, tab_id)
);

create index if not exists learning_tabs_artist_slug_idx on public.learning_tabs(artist_slug);
create index if not exists learning_tabs_status_created_at_idx on public.learning_tabs(status, created_at desc);
create index if not exists learning_tab_tracks_tab_id_position_idx on public.learning_tab_tracks(tab_id, position);
create index if not exists learning_playlists_user_id_created_at_idx on public.learning_playlists(user_id, created_at desc);
create index if not exists learning_history_user_id_opened_at_idx on public.learning_history(user_id, opened_at desc);

