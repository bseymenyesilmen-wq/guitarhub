alter table public.learning_tab_revisions enable row level security;
alter table public.learning_playlists enable row level security;
alter table public.learning_playlist_items enable row level security;
alter table public.learning_favorites enable row level security;
alter table public.learning_history enable row level security;

drop policy if exists "Users can read published learning tabs" on public.learning_tabs;
create policy "Users can read published learning tabs"
  on public.learning_tabs for select
  using (status = 'published' or contributor_id = auth.uid());

drop policy if exists "Users can create own learning tabs" on public.learning_tabs;
create policy "Users can create own learning tabs"
  on public.learning_tabs for insert
  with check (contributor_id = auth.uid());

drop policy if exists "Users can update own learning tabs" on public.learning_tabs;
create policy "Users can update own learning tabs"
  on public.learning_tabs for update
  using (contributor_id = auth.uid())
  with check (contributor_id = auth.uid());

drop policy if exists "Users can read tracks for readable learning tabs" on public.learning_tab_tracks;
create policy "Users can read tracks for readable learning tabs"
  on public.learning_tab_tracks for select
  using (exists (select 1 from public.learning_tabs where learning_tabs.id = learning_tab_tracks.tab_id and (learning_tabs.status = 'published' or learning_tabs.contributor_id = auth.uid())));

drop policy if exists "Users can manage tracks for own learning tabs" on public.learning_tab_tracks;
create policy "Users can manage tracks for own learning tabs"
  on public.learning_tab_tracks for all
  using (exists (select 1 from public.learning_tabs where learning_tabs.id = learning_tab_tracks.tab_id and learning_tabs.contributor_id = auth.uid()))
  with check (exists (select 1 from public.learning_tabs where learning_tabs.id = learning_tab_tracks.tab_id and learning_tabs.contributor_id = auth.uid()));

drop policy if exists "Users can read revisions for readable learning tabs" on public.learning_tab_revisions;
create policy "Users can read revisions for readable learning tabs"
  on public.learning_tab_revisions for select
  using (exists (select 1 from public.learning_tabs where learning_tabs.id = learning_tab_revisions.tab_id and (learning_tabs.status = 'published' or learning_tabs.contributor_id = auth.uid())));

drop policy if exists "Users can create revisions for own learning tabs" on public.learning_tab_revisions;
create policy "Users can create revisions for own learning tabs"
  on public.learning_tab_revisions for insert
  with check (exists (select 1 from public.learning_tabs where learning_tabs.id = learning_tab_revisions.tab_id and learning_tabs.contributor_id = auth.uid()));

drop policy if exists "Users can manage own learning playlists" on public.learning_playlists;
create policy "Users can manage own learning playlists"
  on public.learning_playlists for all
  using (auth.uid() = user_id)
  with check (auth.uid() = user_id);

drop policy if exists "Users can manage own learning playlist items" on public.learning_playlist_items;
