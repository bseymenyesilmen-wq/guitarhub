  using (exists (select 1 from public.learning_playlists where learning_playlists.id = learning_playlist_items.playlist_id and learning_playlists.user_id = auth.uid()))
  with check (exists (select 1 from public.learning_playlists where learning_playlists.id = learning_playlist_items.playlist_id and learning_playlists.user_id = auth.uid()));

drop policy if exists "Users can manage own learning favorites" on public.learning_favorites;
